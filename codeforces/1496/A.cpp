    for (int i = o; i < n; i++)  \
    {                            \
        cin >> (a[i]);           \
    }

void solve()
{
    int n, k;
    cin >> n >> k;

    string s;
    cin >> s;
    if (n - 2 * k <= 0)
    {
        cout << "NO" << endl;
        return;
    }
    for (int i = 0; i < k; ++i)
    {
        if (s[i] != s[n - i - 1])
        {
            cout << "NO" << endl;
            return;
        }
    }
    cout << "YES" << endl;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--)
    {
        solve();
    }

    return 0;
}
