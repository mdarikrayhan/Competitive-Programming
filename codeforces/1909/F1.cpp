int mod = 998244353;
int ma(int a, int b){
    return ((a%mod) + (b%mod))%mod;
}

int mm(int a, int b){
    return ((a%mod)*(b%mod))%mod;
}
void solve()
{
    int n;cin>>n;
    vector<int>a(n+1);
    for(int i = 1;i<=n;++i)cin >> a[i];
    int res = 1;
    for(int i = 1;i<=n;++i){
        int k = a[i]-a[i-1];
        if(a[i] > i){
            res = 0;break;
        }
        if(k < 0 || k >= 3){
            res = 0;break;
        }
        if(k==0)continue;
        int x = (i-1)-(a[i-1]);

        if(k==2){
            res = mm(res, mm(x,x));
        }else res = mm(res, 1+mm(2,x));
    }
    if(a[n]!=n)res= 0;
    cout << res << endl;
}   

